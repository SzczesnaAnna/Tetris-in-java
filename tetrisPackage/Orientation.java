package tetrisPackage;

/**
*	Enum <code>Orientation</code> is a class that stores the references to cardinal directions,
*	that are used to move and orient the tetronimo.
*	@author Mako
*	@version 0.9.5
*/

public enum Orientation
{
	/**
	 *	Direction of upper screen edge.
	 */
	UP,
	/**
	 *	Direction of right-hand-side screen edge.
	 */
	RIGHT,
	/**
	 *	Direction of lower screen edge.
	 */
	DOWN,
	/**
	 *	Direction of left-hand-side screen edge.
	 */
	LEFT
}