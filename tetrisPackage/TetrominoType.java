package tetrisPackage;

/**
*	Enum <code>TetronimoType</code> is a class that stores the references to tetronimo names,
*	that are used to select and differentiate the tetronimo.
*	@author Mako
*	@version 0.9.5
*/

public enum TetrominoType
{
	/**
	 *	The strait tetronimo in an I-shape.
	 */
	I,
	/**
	 *	The bent tetronimo in a L-shape.
	 */
	L,
	/**
	 *	The bent tetronimo mirroring a L-shape.
	 */
	J,
	/**
	 *	The cross tetronimo in a T-shape.
	 */
	T,
	/**
	 *	The twice-bent tetronimo in a S-shape.
	 */
	S,
	/**
	 *	The twice-bent tetronimo in a Z-shape.
	 */
	Z,
	/**
	 *	The square tetronimo in an O-shape.
	 */
	O
}