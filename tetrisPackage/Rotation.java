package tetrisPackage;

/**
*	Enum <code>Rotation</code> is a class that stores the references to rotational directions,
*	that are used to rotate and orient the tetronimo.
*	@author Mako
*	@version 0.9.5
*/

public enum Rotation
{
	/**
	 *	Indicator of the "right" side of the tetronimo.
	 */
	CLOCKWISE,
	/**
	 *	Indicator of the "left" side of the tetronimo.
	 */
	COUNTERCLOCKWISE
}